package CodigoFuente;

public class Multimedia {
	public static String Nombre;
	public static String Genero;
	public static int Año_estreno;
	public static String Clasificacion;

	public static String getNombre() {
		return Nombre;
	}

	public static void setNombre(String nombre) {
		Nombre = nombre;
	}

	public static String getGenero() {
		return Genero;
	}

	public static void setGenero(String genero) {
		Genero = genero;
	}

	public static int getAño_estreno() {
		return Año_estreno;
	}

	public static void setAño_estreno(int año_estreno) {
		Año_estreno = año_estreno;
	}

	public static String getClasificacion() {
		return Clasificacion;
	}

	public static void setClasificacion(String clasificacion) {
		Clasificacion = clasificacion;
	}
}
